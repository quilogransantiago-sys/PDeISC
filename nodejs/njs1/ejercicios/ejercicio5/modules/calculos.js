// calculos.js
// Propósito: Módulo reutilizable con funciones aritméticas básicas
// Exporta: sumar, restar, multiplicar, dividir

export function sumar(a, b) {
    return a + b;
}

export function restar(a, b) {
    return a - b;
}

export function multiplicar(a, b) {
    return a * b;
}

export function dividir(a, b) {
    return a / b;
}