function sumar(a, b) {
    return a + b;
}

function restar(a, b) {
    return a - b;
}

function multiplicar(a, b) {
    return a * b;
}

function dividir(a, b) {
    return a / b;
}

console.log("4 + 5 = " + sumar(4, 5));
console.log("3 - 6 = " + restar(3, 6));
console.log("2 * 7 = " + multiplicar(2, 7));
console.log("20 / 4 = " + dividir(20, 4));