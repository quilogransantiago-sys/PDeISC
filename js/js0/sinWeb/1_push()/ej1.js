// --- EJERCICIO 1: Crear un array vacío y agregar tres frutas usando push() ---

// 1. Declaramos un array vacío
let frutas = [];

// 2. Usamos push() para agregar tres frutas una por una (o todas juntas)
// Explicación: push() mete elementos al final. El array queda ["manzana", "pera", "fresa"]
frutas.push("manzana");
frutas.push("pera");
frutas.push("fresa");

// También puede hacer frutas.push("manzana", "pera", "fresa");

// 3. Mostramos el resultado en la consola
console.log("Array de frutas después de push():", frutas);